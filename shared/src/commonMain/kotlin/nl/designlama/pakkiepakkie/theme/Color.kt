// Win-chance gauge arc (red → orange → green)
val GaugeLow = Color(0xFFDC2626)
val GaugeMid = Color(0xFFF97316)
val GaugeHigh = Color(0xFF22C55E)
val GaugeLowDark = Color(0xFFF87171)
val GaugeMidDark = Color(0xFFFB923C)
val GaugeHighDark = Color(0xFF4ADE80)

fun winChanceColor(percent: Float, darkTheme: Boolean): Color {
    val p = percent.coerceIn(0f, 100f)
    val low = if (darkTheme) GaugeLowDark else GaugeLow
    val mid = if (darkTheme) GaugeMidDark else GaugeMid
    val high = if (darkTheme) GaugeHighDark else GaugeHigh
    return when {
        p <= 50f -> lerp(low, mid, p / 50f)
        else -> lerp(mid, high, (p - 50f) / 50f)
    }
}

